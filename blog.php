    $(document).ready(function(){
        $('#alert_bloglist').hide();
        $('#loadmore').click( function() {
            $('#loadmore').hide();
            $('#preloader').show();
            
            var page = $('#page').html();
            var csrfToken = $('meta[name="csrf-token"]').attr('content');
            
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': csrfToken
                }
            });

            nextpage = parseInt(page) + 1;
            $('#page').html(nextpage);

            $.ajax({
                type: 'GET', // define the type of HTTP verb we want to use (POST for our form)
                url: APP_URL + 'blog/get-list', // the url where we want to POST
                data: {
                    page: nextpage
                }, // our data object
                dataType: 'json', // what type of data do we expect back from the server
                encode: true,
                success: function (result) {
                    
                    if(result.status){
                        $('#blogpost-list').append(result.data);
                        $('#loadmore').show();
                    }else{
                        $('#alert_bloglist').show();
                        $('#alert_bloglist > .error_message').html(result.message);
                        setTimeout(function(){
                            $('#alert_bloglist').fadeOut('slow'); 
                        }, 5*1000);
                    }

                    $('#preloader').hide();
                },
                error: function (XMLHttpRequest, textStatus, errorThrown) {
                    $('#alert_bloglist').show();
                    $('#alert_bloglist > .error_message').html(result.message);
                    setTimeout(function(){
                        $('#alert_bloglist').fadeOut('slow'); 
                    }, 5*1000);

                    $('#preloader').hide();
                }
            });
        });

    });
</script>
    


